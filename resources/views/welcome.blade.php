                    <h5>THE FIRM</h5>
<p><span class="brand">Mark <span class="and">&amp;</span> Arch</span> is an independent organization providing consultancy and advisory services in the fields of Architecture, Civil and Structural engineering.
<span class="brand">Mark <span class="and">&amp;</span> Arch</span> has well qualified engineers and technicians who have considerable expertise in both traditional and high-tech engineering. Through its key personnel <span class="brand">Mark <span class="and">&amp;</span> Arch</span> provides a personal and unbiased service to every individual client and deals with every project, large or small, with a high degree of aesthetic architecture and engineering quality and efficiency.</p>

<p>The Company is capable of handling a wide variety of projects, including development engineering, conceptual engineering, detailed design, construction and project management. The firm has advised other consulting engineering firms, developers and contractors on the construction and design of some technically advanced Civil Engineering Structures.</p>

<p><span class="brand">Mark <span class="and">&amp;</span> Arch</span> has also carried out pre-conceptual and conceptual designs on multi-disciplinary
installations involving the provision of detailed cost analyses and projected programme schedules. In this respect <span class="brand">Mark <span class="and">&amp;</span> Arch</span> has been able to advise clients on considerations such as de-manning, refurbishment and upgrading of existing facilities as well as new developments.</p>

<p><span class="brand">Mark <span class="and">&amp;</span> Arch</span> has the capability, resources and experience to provide high quality multidisciplinary engineering and project management both onshore and offshore. Our services can begin with preliminary planning and extend through master planning, design, contract document preparation, and construction management and monitoring. </p>

<p>These services are supported by extensive in-house computer facilities offering both analysis and computer-aided drafting.
By combining civil, architectural, structural, environmental engineering and management skills into selected project teams, we are able to provide a full range of services for complex infrastructure projects.</p>

<p>The office is located in Badore,  Ajah, Lagos equipped with computers, and other printing facilities.</p>

<h5>OUR DESIGN PHILOSOPHY</h5>
<p>Within the expanding range of engineering, our well known civil and structural divisions still enjoy the continuity of growth seen in past years. However, as each year passes, these activities become a smaller proportion of the whole as our disciplines enjoy similar success in providing the ideas and answers our clients require.</p>

<p>The Company has a reputation of practicing engineering in the broadest sense. We strongly believe in the art of engineering in tandem with figurative analysis and encourage our personnel to take a broad approach and have a healthy understanding of the needs of other disciplines. An understanding of the aims of others and a will to help achieve ideals produces good relations
between team members. This greatly assists in helping unite the whole design process and produce a well integrated advanced project design and analysis that all can be proud of.</p>



<p><span class="brand">Mark <span class="and">&amp;</span> Arch</span> have, in both the past and present, been fortunate to have worked with some of the leading professionals in the industry and have a depth of experience drawn from this. The projects illustrated range from the most classical and historic to the leading edge of modern design. The diversification of skills enables us to undertake projects that range from the largest to the very small with the same care and enthusiasm to achieve quality with best value.</p>

<p>The diversity and experience of our busy practice continues to gain us valuable knowledge in the new production techniques now available to the industry. We have seen that these improving techniques are very capable of providing the skilled engineer with the tools necessary to achieve designs of quality that can be constructed with speed and economy.
The practice is well versed in producing ideas and innovation for new and improved construction techniques and is able to combine advice on both theoretical and practical aspects of these activities.</p>

<p>At <span class="brand">Mark <span class="and">&amp;</span> Arch</span>, we aim to use our ideas, experience and techniques to produce for our clients, total satisfaction of their aims. With all our team colleagues in other disciplines we also strive to enjoy the experience of doing so.</p>


